class Solution {
public:
    void recurse(Node* root) {
        if (root == nullptr) return;
        
        for (auto a : root->children) {
            recurse(a);
        }
        
        ret.push_back(root->val);
    }
    
    vector<int> postorder(Node* root) {
        recurse(root);
        return ret;
    }
    
    vector<int> ret;
};