class Solution {
public:
    void order(Node *root, int level) {
        if (root == nullptr) return;
        
        if (ret.size() < (level+1)) {
            vector<int> temp;
            ret.push_back(temp);
        }
        
        ret.at(level).push_back(root->val);
        
        for (auto a : root->children) {
            order(a, level+1);
        }
    }

    vector<vector<int>> levelOrder(Node* root) {
        order(root, 0);
        
        return ret;
    }
    
    vector<vector<int>> ret;
};